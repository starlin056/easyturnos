-- Volcando estructura para procedimiento db_relojito.ver_reporte_servicio
DELIMITER //
CREATE PROCEDURE `ver_reporte_servicio`(
	IN `v_fechainicio` VARCHAR(50),
	IN `v_fechafin` VARCHAR(50),
	IN `v_accion` VARCHAR(50)
)
BEGIN

IF v_accion = 'S' THEN
SELECT s.nombre_sede,se.nombre_servicio, COUNT(t.turno) AS cantidad
FROM db_turnos t
INNER JOIN db_sedes s ON t.turno_sede=s.id_sede
INNER JOIN db_servicios se ON t.tipo_tramite=se.id
WHERE  DATE_FORMAT(t.tiempo_ingreso, '%Y-%m-%d') between v_fechainicio and v_fechafin
GROUP BY t.tipo_tramite;
END IF;
IF v_accion = 'A' THEN
SELECT 
    YEAR(t.tiempo_ingreso) AS anio,
    CASE
    WHEN MONTH(t.tiempo_ingreso) = 1 THEN 'Enero'
    WHEN MONTH(t.tiempo_ingreso) = 2 THEN 'Febrero'
    WHEN MONTH(t.tiempo_ingreso) = 3 THEN 'Marzo'
    WHEN MONTH(t.tiempo_ingreso) = 4 THEN 'Abril'
    WHEN MONTH(t.tiempo_ingreso) = 5 THEN 'Mayo'
    WHEN MONTH(t.tiempo_ingreso) = 6 THEN 'Junio'
    WHEN MONTH(t.tiempo_ingreso) = 7 THEN 'Julio'
    WHEN MONTH(t.tiempo_ingreso) = 8 THEN 'Agosto'
    WHEN MONTH(t.tiempo_ingreso) = 9 THEN 'Septiembre'
    WHEN MONTH(t.tiempo_ingreso) = 10 THEN 'Octubre'
    WHEN MONTH(t.tiempo_ingreso) = 11 THEN 'Noviembre'
    WHEN MONTH(t.tiempo_ingreso) = 12 THEN 'Diciembre'
    END AS mes,s.nombre_sede,count(t.turno) AS cantidad
    FROM db_turnos t
    INNER JOIN db_sedes s ON t.turno_sede=s.id_sede
   WHERE  DATE_FORMAT(t.tiempo_ingreso, '%Y-%m-%d') between v_fechainicio and v_fechafin
    GROUP BY MONTH(t.tiempo_ingreso), t.turno_sede;
END IF;
END//
